// This line includes the iostream library, which is necessary
// for input and output operations, like printing to the console.
#include <iostream>

// The main function is the entry point of every C++ program.
// Execution starts here.
int main() {
    // std::cout is the standard output stream.
    // The << operator is used to send the string "Hello, World!" to it.
    // std::endl is a manipulator that creates a new line and flushes the output buffer.
    std::cout << "Hello, World!" << std::endl;

    // The return 0 statement indicates that the program has executed successfully.
    return 0;
}